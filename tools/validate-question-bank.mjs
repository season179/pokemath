#!/usr/bin/env node
// Offline validation gate CLI (M4, #14): proves a question bank is fit to
// enter an approved bank. Runs the hand-rolled gate (structural + mechanical
// + adversarial, shared/question-gate.ts) AND AJV against the editor-facing
// JSON schema (schemas/question-bank-vN.schema.json), so the runtime contract
// and the published schema are proven in lockstep.
//
// Usage:
//   node tools/validate-question-bank.mjs [bank.json] [--out <dir>] [--no-write]
//
// Evidence: writes <bank_id>.v<version>.gate.json and .gate.md (accept/reject
// verdict, per-checklist-row results, every finding) into --out (default
// docs/question-banks/). Exit code 0 = accept, 1 = reject or unreadable input.

import { createHash } from "node:crypto";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { basename, dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

import Ajv2020 from "ajv/dist/2020.js";

import {
  CORPUS_CHECKLIST,
  gateQuestionBank,
} from "../shared/question-gate.ts";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
export const DEFAULT_BANK_PATH = join(
  root,
  "game",
  "assets",
  "resources",
  "question-banks",
  "std1",
  "woolly-meadows.v1.json",
);
export const DEFAULT_OUT_DIR = join(root, "docs", "question-banks");

/** Resolve the JSON-schema file that mirrors the bank's schema_version. */
export function schemaPathFor(schemaVersion) {
  if (schemaVersion === 1 || schemaVersion === 2) {
    return join(root, "schemas", `question-bank-v${schemaVersion}.schema.json`);
  }
  return null;
}

/**
 * AJV against the published JSON schema (answer-form and figure payload
 * shapes). Returns GateFinding-shaped rows; an unknown schema version yields
 * a single error row (the hand-rolled gate reports the same failure).
 */
export async function checkJsonSchema(raw) {
  const schemaVersion =
    raw && typeof raw === "object" && !Array.isArray(raw) ? raw.schema_version : undefined;
  const schemaPath = schemaPathFor(schemaVersion);
  if (!schemaPath) {
    return [
      {
        phase: "structural",
        rule: "SCHEMA-V2",
        severity: "error",
        code: "json-schema",
        message: `no published JSON schema for schema_version ${JSON.stringify(schemaVersion)}`,
      },
    ];
  }
  const schema = JSON.parse(await readFile(schemaPath, "utf8"));
  const ajv = new Ajv2020({ allErrors: true });
  const validate = ajv.compile(schema);
  if (validate(raw)) return [];
  return (validate.errors ?? []).map((e) => ({
    phase: "structural",
    rule: "SCHEMA-V2",
    severity: "error",
    code: "json-schema",
    message: `${e.instancePath || "/"} ${e.message ?? "schema violation"}`,
  }));
}

/** Checklist-row rollup: for each corpus row, pass / warn / fail + evidence. */
export function checklistResults(report, jsonSchemaFindings) {
  const all = [...jsonSchemaFindings, ...report.phases.flatMap((p) => p.findings)];
  return CORPUS_CHECKLIST.map((row) => {
    const findings = all.filter((f) => f.rule === row.rule);
    const errors = findings.filter((f) => f.severity === "error").length;
    const warns = findings.filter((f) => f.severity === "warn").length;
    return {
      ...row,
      result: errors > 0 ? "fail" : warns > 0 ? "warn" : "pass",
      findings,
    };
  });
}

function cell(text) {
  return String(text).replaceAll("|", "\\|").replaceAll("\n", "<br>");
}

/** Human-facing accept/reject evidence. */
export function renderGateReportMarkdown(report, jsonSchemaFindings, sourceText, sourcePath) {
  const hash = createHash("sha256").update(sourceText).digest("hex");
  const rows = checklistResults(report, jsonSchemaFindings);
  const title = report.bankId ?? basename(sourcePath);
  const lines = [
    `# Validation gate report — ${title}`,
    "",
    `> Generated by tools/validate-question-bank.mjs. Do not edit; re-run the gate.`,
    "",
    `**Verdict:** ${report.accept ? "✅ ACCEPT" : "❌ REJECT"}`,
    `**Bank:** \`${title}\`${report.bankVersion ? ` v${report.bankVersion}` : ""} · schema v${report.schemaVersion ?? "?"} · ${report.questionCount} questions`,
    `**Source:** [${basename(sourcePath)}](../../game/assets/resources/question-banks/std1/${basename(sourcePath)})`,
    `**Content SHA-256:** \`${hash}\``,
    "",
    "## Corpus checklist",
    "",
    "| Rule | Phase | Result | Evidence |",
    "|---|---|---|---|",
    ...rows.map(
      (r) =>
        `| ${r.rule} | ${r.phase} | ${r.result === "pass" ? "✅" : r.result === "warn" ? "⚠️" : "❌"} | ${cell(r.description)} |`,
    ),
    "",
  ];
  const failed = rows.filter((r) => r.result !== "pass");
  if (failed.length === 0) {
    lines.push("Every checklist row passed. The bank may enter an approved bank.", "");
  } else {
    lines.push("## Findings", "");
    for (const row of failed) {
      lines.push(`### ${row.rule} — ${row.result === "fail" ? "❌" : "⚠️"}`, "");
      for (const f of row.findings) {
        lines.push(`- ${f.severity === "error" ? "❌" : "⚠️"} ${f.id === undefined ? "" : `Q${f.id} · `}\`${f.code}\` — ${f.message}`);
      }
      lines.push("");
    }
    lines.push("The gate never edits content: regenerate or fix the source batch and re-run.", "");
  }
  return lines.join("\n");
}

/** Full evidence bundle (JSON sidecar): report + checklist rollup + AJV rows. */
export function buildEvidence(report, jsonSchemaFindings, sourceText, sourcePath) {
  return {
    tool: "tools/validate-question-bank.mjs",
    generatedAt: new Date().toISOString(),
    source: basename(sourcePath),
    sha256: createHash("sha256").update(sourceText).digest("hex"),
    accept: report.accept && jsonSchemaFindings.every((f) => f.severity !== "error"),
    report,
    jsonSchema: jsonSchemaFindings,
    checklist: checklistResults(report, jsonSchemaFindings).map((r) => ({
      rule: r.rule,
      phase: r.phase,
      source: r.source,
      description: r.description,
      result: r.result,
      findings: r.findings,
    })),
  };
}

/** Validate one bank file; returns the evidence bundle and writes artifacts. */
export async function validateQuestionBankFile(bankPath, opts = {}) {
  const sourceText = await readFile(bankPath, "utf8");
  let raw;
  try {
    raw = JSON.parse(sourceText);
  } catch (e) {
    const report = {
      questionCount: 0,
      accept: false,
      phases: [
        {
          phase: "structural",
          accept: false,
          findings: [
            {
              phase: "structural",
              rule: "SCHEMA-V2",
              severity: "error",
              code: "json-parse",
              message: `bank file is not valid JSON: ${e.message}`,
            },
          ],
        },
        { phase: "mechanical", accept: false, findings: [] },
        { phase: "adversarial", accept: false, findings: [] },
      ],
    };
    return buildEvidence(report, [], sourceText, bankPath);
  }
  const report = gateQuestionBank(raw);
  const jsonSchemaFindings = await checkJsonSchema(raw);
  const evidence = buildEvidence(report, jsonSchemaFindings, sourceText, bankPath);

  if (!opts.noWrite) {
    const outDir = opts.outDir ?? DEFAULT_OUT_DIR;
    await mkdir(outDir, { recursive: true });
    const stem = `${evidence.report.bankId ?? basename(bankPath, ".json")}.v${evidence.report.bankVersion ?? "?"}`;
    await writeFile(join(outDir, `${stem}.gate.json`), JSON.stringify(evidence, null, 2) + "\n");
    await writeFile(
      join(outDir, `${stem}.gate.md`),
      renderGateReportMarkdown(report, jsonSchemaFindings, sourceText, bankPath),
    );
  }
  return evidence;
}

const isMain = process.argv[1] && fileURLToPath(import.meta.url) === join(process.argv[1]);
if (isMain) {
  const args = process.argv.slice(2);
  const noWrite = args.includes("--no-write");
  const outAt = args.indexOf("--out");
  const outDir = outAt === -1 ? undefined : args[outAt + 1];
  const bankPath = args.find((a, i) => !a.startsWith("--") && (outAt === -1 || i !== outAt + 1)) ?? DEFAULT_BANK_PATH;
  try {
    const evidence = await validateQuestionBankFile(bankPath, { noWrite, outDir });
    const counts = evidence.report.phases.map(
      (p) => `${p.phase}: ${p.accept ? "ok" : "REJECT"} (${p.findings.length} findings)`,
    );
    console.log(`gate ${evidence.accept ? "ACCEPT" : "REJECT"} — ${evidence.source}`);
    for (const line of counts) console.log(`  ${line}`);
    if (evidence.jsonSchema.length > 0) {
      console.log(`  json-schema: ${evidence.jsonSchema.length} violations`);
    }
    process.exit(evidence.accept ? 0 : 1);
  } catch (e) {
    console.error(`gate error: ${e.message}`);
    process.exit(1);
  }
}
