test("rollback: repoints after verifying the whole target chain; refuses broken targets", async () => {
  const tree = await makeTree();
  try {
    const { candidatePath, decisionsPath } = await staged(tree, { seed: 909, bankId: "std1.bank-a" });
    await importBatch(candidatePath, decisionsPath, opts(tree, { activate: true }));
    let pointer = parseActiveManifestPointer(await readJson(join(tree.resourcesDir, "question-banks", "active-manifest.json")));
    assert.equal(pointer.manifest, "question-banks/manifest.v2");

    const back = await rollbackManifest("manifest.v1", opts(tree));
    assert.equal(back.manifest, "question-banks/manifest.v1");
    pointer = parseActiveManifestPointer(await readJson(join(tree.resourcesDir, "question-banks", "active-manifest.json")));
    assert.equal(pointer.manifest, "question-banks/manifest.v1");
    // And the rolled-back chain still resolves and verifies.
    const manifest = parseQuestionBankManifest(await readJson(join(tree.resourcesDir, "question-banks", "manifest.v1.json")));
    const entry = resolveManifestEntry(manifest, { grade: "std1", topic: "4.2", profile: "dpk3_2026_core", tpLevel: 2 });
    const bank = parseQuestionBankData(await readJson(join(tree.resourcesDir, `${entry.path}.json`)));
    verifyManifestEntryAgainstBank(entry, bank);

    // A target whose bank file vanished must NOT be selected.
    await assert.rejects(rollbackManifest("manifest.v9", opts(tree)), /ENOENT/);
    pointer = parseActiveManifestPointer(await readJson(join(tree.resourcesDir, "question-banks", "active-manifest.json")));
    assert.equal(pointer.manifest, "question-banks/manifest.v1");
  } finally {
    await rm(tree.dir, { recursive: true, force: true });
  }
});
